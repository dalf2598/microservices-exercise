CREATE TABLE Cuenta (
    cuentaid UUID PRIMARY KEY NOT NULL,
    numerocuenta CHAR(8) NOT NULL UNIQUE,
    tipocuenta VARCHAR(50) NOT NULL,
    saldoinicial DECIMAL(15, 2) NOT NULL,
    estado BOOLEAN NOT NULL,
    clientid UUID NOT NULL
);

CREATE TABLE Movimiento (
    movimientoid UUID PRIMARY KEY NOT NULL,
    fecha BIGINT NOT NULL,
    tipomovimiento VARCHAR(10) CHECK (tipoMovimiento IN ('DEPOSITO', 'RETIRO')) NOT NULL,
    valor DECIMAL(15, 2) NOT NULL,
    saldoinicial DECIMAL(15, 2) NOT NULL,
    saldodisponible DECIMAL(15, 2) NOT NULL,
    cuentaid UUID NOT NULL,
    FOREIGN KEY (cuentaid) REFERENCES Cuenta(cuentaid)
);

